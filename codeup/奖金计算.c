#include <stdio.h>
int main()
{
    double I, jj;
    scanf("%lf",&I);
    if(I<=100000)
    {
        jj=0.1*I;
        printf("%.2f\n",jj);
    }
    else if(100000<I<=200000)
    {
        jj=100000*0.1+(I-100000)*0.075;
        printf("%.2f\n",jj);
    }
    else if(200000<I<=400000)
    {
        jj=100000*0.1+100000*0.075+(I-200000)*0.05;
        printf("%.2f\n",jj);
    }
    else if(400000<I<=600000)
    {
        jj=100000*0.1+100000*0.075+200000*0.05+(I-200000)*0.03;
        printf("%.2f\n",jj);
    }
    else if(600000<I<=1000000)
    {
        jj=100000*0.1+100000*0.075+200000*0.05+200000*0.03+(I-600000)*0.015;
        printf("%.2f\n",jj);
    }
    else if(I>1000000)
    {
        jj=100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(I-600000)*0.01;
        printf("%.2f\n",jj);
    }
}
